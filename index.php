                <h1 class="titulo">Resumen del Proyecto IoT</h1>
                <p>Implementamos un sistema automatizado de recopilación y distribución de datos energéticos y hídricos utilizando tecnologías IoT. Nuestro objetivo es mejorar la eficiencia operativa y la gestión de recursos mediante la precisión en la obtención de datos y la optimización de procesos.</p>
                <h2 class="subtitulo">Beneficios:</h2>
                <ul class="beneficios">
                    <li>Mejora en la precisión de la información recopilada.</li>
                    <li>Reducción de errores humanos en la toma de datos.</li>
                    <li>Agilización del proceso de obtención y distribución de datos.</li>
                    <li>Identificación temprana de problemas y tendencias en el consumo de energía y agua.</li>
                    <li>Mayor seguridad en la transferencia de datos.</li>
                    <li>Acceso a informes detallados sobre el consumo de recursos.</li>
                </ul>
                <h2 class="subtitulo">Impacto en la Eficiencia Operativa:</h2>
                <p>Nuestro sistema permite a las empresas optimizar el uso de recursos, reducir costos operativos y mejorar la calidad del servicio al cliente mediante la automatización de procesos y la entrega eficiente de información relevante.</p>
